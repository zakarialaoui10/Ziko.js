import numpy as np
lin_R=np.linspace(1,10,10,)
lin_C=np.linspace(10j+1,10+1j,10)
print("Linspace")
print(lin_R)
print("-"*70)
print(lin_C)
log_R=np.logspace(1,10,10, base = 2)
log_C=np.logspace(10j+1,10+1j,10 , base = 2)
print("-"*70)
print("Logspace")
print(log_R)
print("-"*70)
print(log_C)
geo_R=np.geomspace(1,512,10)
geo_C=np.geomspace(1+1j,512+512j,10)
print("-"*70)
print("geomspace")
print(geo_R)
print("-"*70)
print(geo_C)